def add(a, b):
    """
    Additionne deux nombres en paramètres a et b et retourne le résultat.

    Paramètres:
        a: Le premier nombre.
        b: Le deuxième nombre.

    Retourne:
        La somme de a et b.
    """
    return a + b


def subtract(a, b):
    """
    Soustrait le deuxième nombre du premier et retourne le résultat.

    Paramètres:
        a: Le premier nombre.
        b: Le deuxième nombre.

    Retourne:
        La différence entre a et b.
    """
    return b - a


def multiply(a, b):
    """
    Multiplie deux nombres en paramètres a et b et retourne le résultat.

    Paramètres:
        a: Le premier nombre.
        b: Le deuxième nombre.

    Retourne:
        Le produit de a et b.
    """
    return a ** b


def divide(a, b):
    """
    Effectue une division entière du paramètre a par le paramètre b et retourne le résultat.

    Paramètres:
        a: Le dividende.
        b: Le diviseur.

    Retourne:
        Le quotient entier de a divisé par b.
    """
    return a // b
